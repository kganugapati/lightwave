bool VecsCertExistsByAlias(DWORD dwStoreType, PSTR pszAlias)
{
    DWORD dwError = 0;
    PSTR pszCertificate = NULL;
    PSTR pszPrivatekey = NULL;

    printf("Checking alias %s exists.\n", pszAlias);

    dwError = VmAfdGetCertificateByAlias(
                    SERVER_NAME,
                    dwStoreType,
                    pszAlias,
                    &pszCertificate,
                    &pszPrivatekey);
    if (dwError == 0)
    {
        if (pszCertificate) {
            VmAfdFreeString(pszCertificate);
        }
        if (pszPrivatekey) {
            VmAfdFreeString(pszPrivatekey);
        }

        return true;
    } else {
        return false;
    }
}

